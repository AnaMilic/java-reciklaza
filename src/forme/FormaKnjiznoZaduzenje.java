    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        if(tblKnjZad.getSelectedRow()<0){
            JOptionPane.showMessageDialog(this, "Potrebno je da izaberete knjizno zaduzenje koje zelite da izmenite", "Greska!", JOptionPane.ERROR_MESSAGE);
        }else{
            
            try {
                List<Knjizno_Zaduzenje> listaKZ=Kontroler.getInstanca().listaKnjiznihZaduzenja();
                
                Knjizno_Zaduzenje kz=((TabelaKnjiznoZaduzenje)tblKnjZad.getModel()).getSelectedObject(tblKnjZad.getSelectedRow());
                FormaKnjiznoZaduzenjeDodaj fkzd=new FormaKnjiznoZaduzenjeDodaj(this,true,kz,"UPDATE",listaKZ);
                fkzd.setVisible(true);
            } catch (Exception ex) {
                Logger.getLogger(FormaKnjiznoZaduzenje.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnIzmeniActionPerformed

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        if(tblKnjZad.getSelectedRow()<0){
            JOptionPane.showMessageDialog(this, "Potrebno je da izaberete knjizno zaduzenje koje zelite da obrisete", "Greska!", JOptionPane.ERROR_MESSAGE);
        }else{
            try {
                Knjizno_Zaduzenje kz=((TabelaKnjiznoZaduzenje)tblKnjZad.getModel()).getSelectedObject(tblKnjZad.getSelectedRow());
                Kontroler.getInstanca().delete(kz);
                JOptionPane.showMessageDialog(this, "Uspesno ste obrisali knjizno zaduzenje!");
                refreshTbl();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Greska prilikom brisanja knjiznog zaduzenja", "Greska!", JOptionPane.ERROR_MESSAGE);
            } 
        }
    }//GEN-LAST:event_btnObrisiActionPerformed

