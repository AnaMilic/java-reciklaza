    private void popuniTabelu() {
        try {
            List<Knjizno_Zaduzenje> listaZaduzenja=Kontroler.getInstanca().listaKnjiznihZaduzenja();
            TabelaKnjiznoZaduzenje tblKZ=new TabelaKnjiznoZaduzenje(listaZaduzenja);
            tblKnjZad.setModel(tblKZ);
        } catch (Exception ex) {
            Logger.getLogger(FormaArtikal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    public void refreshTbl(){
        List<Knjizno_Zaduzenje> shownKz=loadObjects();
        TabelaKnjiznoZaduzenje tbl=(TabelaKnjiznoZaduzenje) tblKnjZad.getModel();
        tbl.setList(shownKz);
        tbl.fireTableDataChanged();
    }

    private List<Knjizno_Zaduzenje> loadObjects() {
        try {
            return Kontroler.getInstanca().listaKnjiznihZaduzenja();
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        return null;
    }
