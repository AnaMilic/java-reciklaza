    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        try {
            List<Clan> listaOsnovno=Kontroler.getInstanca().listaClanova(z);
            
            
            int brojClanova=0;
            for (Clan cl : listaOsnovno) {
                brojClanova=Integer.max(0, cl.getBrojClana());
            }
            
            Clan c=new Clan();
            FormaClanDodaj fcd=new FormaClanDodaj(c,"INSERT",brojClanova,z);
            fcd.setVisible(true);
            this.setVisible(false);
        } catch (Exception ex) {
            Logger.getLogger(FormaClan.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnDodajActionPerformed

