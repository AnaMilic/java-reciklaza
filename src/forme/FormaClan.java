    private void popuniTabele(Zakon z) {
        try {
            Clan c=new Clan();
            c.setTabela(1);
            List<Clan> listaOsnovno=Kontroler.getInstanca().listaClanova(z);
            TabelaClanOsnovno tblOsnovno=new TabelaClanOsnovno(listaOsnovno);
            tblClanOsnovno.setModel(tblOsnovno);
            
            c.setTabela(2);
            List<Clan> listaDetalji=Kontroler.getInstanca().listaClanova(z);
            TabelaClanDetalji tblDetalji=new TabelaClanDetalji(listaDetalji);
            tblClanDetalji.setModel(tblDetalji);
        } catch (Exception ex) {
            Logger.getLogger(FormaClan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

