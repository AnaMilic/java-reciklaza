    private void popuniTabelu() {
        try {
            List<Dobavljac> listaDobavljaca=Kontroler.getInstanca().listaDobavljaca();
            TabelaDobavljac tblDob=new TabelaDobavljac(listaDobavljaca);
            tblDobavljac.setModel(tblDob);
        } catch (Exception ex) {
            Logger.getLogger(FormaArtikal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    public void refreshTbl(){
        List<Dobavljac> shownD=loadObjects();
        TabelaDobavljac tbl=(TabelaDobavljac) tblDobavljac.getModel();
        tbl.setList(shownD);
        tbl.fireTableDataChanged();
    }

    private List<Dobavljac> loadObjects() {
        try {
            return Kontroler.getInstanca().listaDobavljaca();
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        return null;
    }
