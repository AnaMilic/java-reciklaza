    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        if(tblDobavljac.getSelectedRow()<0){
            JOptionPane.showMessageDialog(this, "Potrebno je da izaberete dobavljaca kog zelite da izmenite", "Greska!", JOptionPane.ERROR_MESSAGE);
        }else{
            Dobavljac d=((TabelaDobavljac)tblDobavljac.getModel()).getSelectedObject(tblDobavljac.getSelectedRow());
            FormaDobavljacDodaj fdd=new FormaDobavljacDodaj(this,true,d,"UPDATE");
            fdd.setVisible(true);
        } 
    }//GEN-LAST:event_btnIzmeniActionPerformed

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        if(tblDobavljac.getSelectedRow()<0){
            JOptionPane.showMessageDialog(this, "Potrebno je da izaberete dobavljaca kog zelite da obrisete", "Greska!", JOptionPane.ERROR_MESSAGE);
        }else{
            try {
                Dobavljac d=((TabelaDobavljac)tblDobavljac.getModel()).getSelectedObject(tblDobavljac.getSelectedRow());
                Kontroler.getInstanca().delete(d);
                JOptionPane.showMessageDialog(this, "Uspesno ste obrisali dobavljaca!");
                refreshTbl();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Greska prilikom brisanja dobavljaca", "Greska!", JOptionPane.ERROR_MESSAGE);
            } 
        }
    }//GEN-LAST:event_btnObrisiActionPerformed

