    private void popuniTabelu() {
        try {
            List<Cena_Artikla> listaCena=Kontroler.getInstanca().listaCenaArtikla();
            TabelaCenaArtikla tblCA=new TabelaCenaArtikla(listaCena);
            tblCena.setModel(tblCA);
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
    public void refreshTbl(){
        List<Cena_Artikla> shownC=loadObjects();
        TabelaCenaArtikla tbl=(TabelaCenaArtikla) tblCena.getModel();
        tbl.setList(shownC);
        tbl.fireTableDataChanged();
    }

    private List<Cena_Artikla> loadObjects() {
        try {
            return Kontroler.getInstanca().listaCenaArtikla();
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        return null;
    }
