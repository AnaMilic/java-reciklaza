    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        if(tblCena.getSelectedRow()<0){
            JOptionPane.showMessageDialog(this, "Potrebno je da izaberete cenu koju zelite da izmenite", "Greska!", JOptionPane.ERROR_MESSAGE);
        }else{
            Cena_Artikla ca=((TabelaCenaArtikla)tblCena.getModel()).getSelectedObject(tblCena.getSelectedRow());
            FormaCenaArtiklaDodaj fcda=new FormaCenaArtiklaDodaj(this, true, ca, "UPDATE");
            fcda.setVisible(true);
        }
    }//GEN-LAST:event_btnIzmeniActionPerformed

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        if(tblCena.getSelectedRow()<0){
            JOptionPane.showMessageDialog(this, "Potrebno je da izaberete cenu koju zelite da obrisete", "Greska!", JOptionPane.ERROR_MESSAGE);
        }else{
            try {
                Cena_Artikla ca=((TabelaCenaArtikla)tblCena.getModel()).getSelectedObject(tblCena.getSelectedRow());
                Kontroler.getInstanca().delete(ca);
                JOptionPane.showMessageDialog(this, "Uspesno ste obrisali cenu artikla!");
                refreshTbl();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska!", JOptionPane.ERROR_MESSAGE);
            }
            
        }
    }//GEN-LAST:event_btnObrisiActionPerformed

