    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        if(tblKupac.getSelectedRow()<0){
            JOptionPane.showMessageDialog(this, "Potrebno je da izaberete kupca kog zelite da izmenite", "Greska!", JOptionPane.ERROR_MESSAGE);
        }else{
            Kupac k=((TabelaKupac)tblKupac.getModel()).getSelectedObject(tblKupac.getSelectedRow());
            FormaKupacDodaj fkd=new FormaKupacDodaj(this,true,k,"UPDATE");
            fkd.setVisible(true);
        }      
    }//GEN-LAST:event_btnIzmeniActionPerformed

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        if(tblKupac.getSelectedRow()<0){
            JOptionPane.showMessageDialog(this, "Potrebno je da izaberete kupca kog zelite da obrisete", "Greska!", JOptionPane.ERROR_MESSAGE);
        }else{
            try {
                Kupac k=((TabelaKupac)tblKupac.getModel()).getSelectedObject(tblKupac.getSelectedRow());
                Kontroler.getInstanca().delete(k);
                JOptionPane.showMessageDialog(this, "Uspesno ste obrisali kupca!");
                refreshTbl();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Greska prilikom brisanja kupca", "Greska!", JOptionPane.ERROR_MESSAGE);
            } 
        }
    }//GEN-LAST:event_btnObrisiActionPerformed

