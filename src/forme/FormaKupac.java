    private void popuniTabelu() {
        try {
            List<Kupac> listaKupaca=Kontroler.getInstanca().listaKupaca();
            TabelaKupac tblKpc=new TabelaKupac(listaKupaca);
            tblKupac.setModel(tblKpc);
        } catch (Exception ex) {
            Logger.getLogger(FormaArtikal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    public void refreshTbl(){
        List<Kupac> shownK=loadObjects();
        TabelaKupac tbl=(TabelaKupac) tblKupac.getModel();
        tbl.setList(shownK);
        tbl.fireTableDataChanged();
    }

    private List<Kupac> loadObjects() {
        try {
            return Kontroler.getInstanca().listaKupaca();
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        return null;
    }
