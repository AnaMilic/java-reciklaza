    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        try {
            List<Prijemnica> listaPrij=Kontroler.getInstanca().listaPrijemnica();
            
            int brojPrijemnice=1;
            for (Prijemnica pr : listaPrij) {
                brojPrijemnice=Integer.max(1, pr.getBrojPrijemnice());
            }
            
            
            
            Prijemnica p=new Prijemnica();
            FormaPrijemnicaDodaj fpd=new FormaPrijemnicaDodaj(this,true,p,"INSERT",brojPrijemnice);
            fpd.setVisible(true);
        } catch (Exception ex) {
            Logger.getLogger(FormaZapisnikOSortiranju.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDodajActionPerformed

    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        if(tblPrijemnica.getSelectedRow()<0){
            JOptionPane.showMessageDialog(this, "Potrebno je da izaberete prijemnicu koju zelite da izmenite", "Greska!", JOptionPane.ERROR_MESSAGE);
        }else{
            Prijemnica p=((TabelaPrijemnica)tblPrijemnica.getModel()).getSelectedObject(tblPrijemnica.getSelectedRow());
            FormaPrijemnicaDodaj fpd=new FormaPrijemnicaDodaj(this,true,p,"UPDATE",0);
            fpd.setVisible(true);
        }
    }//GEN-LAST:event_btnIzmeniActionPerformed

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        if(tblPrijemnica.getSelectedRow()<0){
            JOptionPane.showMessageDialog(this, "Potrebno je da izaberete prijemnicu koju zelite da obrisete", "Greska!", JOptionPane.ERROR_MESSAGE);
        }else{
            try {
                Prijemnica p=((TabelaPrijemnica)tblPrijemnica.getModel()).getSelectedObject(tblPrijemnica.getSelectedRow());
                Kontroler.getInstanca().delete(p);
                JOptionPane.showMessageDialog(this, "Uspesno ste obrisali prijemnicu!");
                refreshTbl();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Greska prilikom brisanja prijemnice", "Greska!", JOptionPane.ERROR_MESSAGE);
            } 
        }
    }//GEN-LAST:event_btnObrisiActionPerformed

    private void btnStavkeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStavkeActionPerformed
        if(tblPrijemnica.getSelectedRow()<0){
            JOptionPane.showMessageDialog(this, "Potrebno je da izaberete prijemnicu za koju zelite da vidite stavke", "Greska!", JOptionPane.ERROR_MESSAGE);
        }else{
            Prijemnica p=((TabelaPrijemnica)tblPrijemnica.getModel()).getSelectedObject(tblPrijemnica.getSelectedRow());
            FormaStavkaPrijemnice fsp=new FormaStavkaPrijemnice(p);
            fsp.setVisible(true);
            
        }
    }//GEN-LAST:event_btnStavkeActionPerformed

