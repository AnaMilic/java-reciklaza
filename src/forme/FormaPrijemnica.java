    private void popuniTabelu() {
        try {
            List<Prijemnica> listaPrijemnica=Kontroler.getInstanca().listaPrijemnica();
            TabelaPrijemnica tblP=new TabelaPrijemnica(listaPrijemnica);
            tblPrijemnica.setModel(tblP);
        } catch (Exception ex) {
            Logger.getLogger(FormaArtikal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    public void refreshTbl(){
        List<Prijemnica> shownP=loadObjects();
        TabelaPrijemnica tbl=(TabelaPrijemnica) tblPrijemnica.getModel();
        tbl.setList(shownP);
        tbl.fireTableDataChanged();
    }

    private List<Prijemnica> loadObjects() {
        try {
            return Kontroler.getInstanca().listaPrijemnica();
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        return null;
    }
