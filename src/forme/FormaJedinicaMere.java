    private void popuniTabelu() {
        try {
            List<Jedinica_Mere> listaJM=Kontroler.getInstanca().listaJM();
            TabelaJedinicaMere tblJedMer=new TabelaJedinicaMere(listaJM);
            tblJM.setModel(tblJedMer);
        } catch (Exception ex) {
            Logger.getLogger(FormaJedinicaMere.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    public void refreshTbl(){
        List<Jedinica_Mere> shownJM=loadObjects();
        TabelaJedinicaMere tbl=(TabelaJedinicaMere) tblJM.getModel();
        tbl.setList(shownJM);
        tbl.fireTableDataChanged();
    }

    private List<Jedinica_Mere> loadObjects() {
        try {
            return Kontroler.getInstanca().listaJM();
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        return null;
    }
