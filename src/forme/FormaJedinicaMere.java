    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        
        try {
            List<Jedinica_Mere> listaJM=Kontroler.getInstanca().listaJM();
            
            int brojJM=1;
            for (Jedinica_Mere j : listaJM) {
                brojJM=Integer.max(1, j.getSifraJediniceMere());
            }
            
            Jedinica_Mere jm=new Jedinica_Mere();
            FormaJedinicaMereDodaj fjmd=new FormaJedinicaMereDodaj(this,true,jm,"INSERT",brojJM);
            fjmd.setVisible(true);
        } catch (Exception ex) {
            Logger.getLogger(FormaJedinicaMere.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnDodajActionPerformed

    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        if(tblJM.getSelectedRow()<0){
            JOptionPane.showMessageDialog(this, "Potrebno je da izaberete jedinicu mere koju zelite da izmenite", "Greska!", JOptionPane.ERROR_MESSAGE);
        }else{
            Jedinica_Mere jm=((TabelaJedinicaMere)tblJM.getModel()).getSelectedObject(tblJM.getSelectedRow());
            FormaJedinicaMereDodaj fjmd=new FormaJedinicaMereDodaj(this,true,jm,"UPDATE",0);
            fjmd.setVisible(true);
        } 
    }//GEN-LAST:event_btnIzmeniActionPerformed

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        if(tblJM.getSelectedRow()<0){
            JOptionPane.showMessageDialog(this, "Potrebno je da izaberete jedinicu mere koju zelite da obrisete", "Greska!", JOptionPane.ERROR_MESSAGE);
        }else{
            try {
                Jedinica_Mere jm=((TabelaJedinicaMere)tblJM.getModel()).getSelectedObject(tblJM.getSelectedRow());
                Kontroler.getInstanca().delete(jm);
                JOptionPane.showMessageDialog(this, "Uspesno ste obrisali jedinicu mere!");
                refreshTbl();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Greska prilikom brisanja jedinice mere", "Greska!", JOptionPane.ERROR_MESSAGE);
            } 
        }
    }//GEN-LAST:event_btnObrisiActionPerformed

